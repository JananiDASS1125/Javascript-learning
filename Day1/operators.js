// OPERATORS

let a = 10;
let b = 5;

// arithmetic
console.log(a + b); //15
console.log(a - b); //5
console.log(a * b); //50
console.log(a / b); //2
console.log(a % b); //0

// comparison
console.log("5" == 5);   // true
console.log("5" === 5);  // false

// logical
console.log(true && false); //false
console.log(true || false); //true
console.log(!true); //false
